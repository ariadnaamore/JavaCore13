package homeworkIterator;

public class Human {
	
//поля класу Human
	private int height;
	private int weight;
	
//Конструктор класу 
	public Human(int height, int weight) {
		super();
		this.height = height;
		this.weight = weight;
	}
	
//Getters, Setters
	public int getHeight() {
		return height;
	}
	public void setHeight(int height) {
		this.height = height;
	}
	public int getWeight() {
		return weight;
	}
	public void setWeight(int weight) {
		this.weight = weight;
	}
	
	
	

}
